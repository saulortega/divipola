// Municipio representa un municipio.
type Municipio string

// Código obtiene el código de 5 dígitos del municipio.
func (M Municipio) Código() string {
	return string(M)
}

// Código3 obtiene los últimos tres dígitos del código del municipio,
// descartando los dos correspondientes a su departamento.
func (M Municipio) Código3() string {
	return string(M)[2:]
}

// Válido comprueba si es un código válido.
func (M Municipio) Válido() bool {
	_, existe := mapaMunicipios[string(M)]
	return existe
}

// Departamento representa el departamento al que pertenece el municipio.
func (M Municipio) Departamento() Departamento {
	return Departamento(string(M)[:2])
}

// Nombre representa el nombre completo del municipio.
func (M Municipio) Nombre() string {
	nombre, existe := mapaMunicipios[string(M)]
	if !existe {
		return "(Desconocido)"
	}

	return nombre
}

func (M Municipio) String() string {
	return M.Nombre()
}

// Municipios lista los municipios.
// Se pueden enviar parámetros adicionales de ordenamiento y filtro.
func Municipios(prmtrs ...interface{}) []Municipio {
	var array = municipiosOrdenadosAlfabéticamente
	var mncps = []Municipio{}

	var orden, filtro = normalizarParámetrosListado(prmtrs...)

	if orden == OrdenNumérico {
		array = municipiosOrdenadosNuméricamente
	}

	if len(filtro) == 0 {
		for _, v := range array {
			mncps = append(mncps, Municipio(v))
		}
	} else {
		for _, v := range array {
			if regexp.MustCompile(filtro).MatchString(normalizarTexto(Municipio(v).String())) || regexp.MustCompile(filtro).MatchString(v) {
				mncps = append(mncps, Municipio(v))
			}
		}
	}

	return mncps
}

